# Faça um Programa que peça um número e então mostre a mensagem O número informado foi [número].

numero = float(input("Informe um número: "))


print(f"O número informado foi {numero:.0f}")

# toda variável/função que se deseja por junto a uma string é necessário estar entre colchetes {} 
# *lembrando que para isso funcionar, é necessário que a string esteja formatada. (f=format)