# Faça um Programa que peça dois números e imprima a soma.

numero_um = float(input("Informe um número:"))
numero_dois = float(input("Informe outro número:"))

soma = numero_um + numero_dois

print(f"Soma dos números é {soma}")

# Outra forma de realizar equação : print(f"Soma dos números é {numero_um + numero_dois}")